// 获取所有教材生词的去重列表（用于搜索等）
export function getAllTextbookWords(): TextbookWord[] {
  const seen = new Set<string>()
  const result: TextbookWord[] = []
  for (const tb of textbookVocabList) {
    for (const lesson of tb.lessons) {
      for (const w of lesson.words) {
        if (!seen.has(w.hanzi)) {
          seen.add(w.hanzi)
          result.push(w)
        }
      }
    }
  }
  return result
}
